CODESG SEGMENT

START:
	MOV	AX,DATASG
	MOV	DS,AX
	MOV	BX,0

	MOV CX,5
S0:
	MOV AL,DS:[BX]
	AND	AL,11011111B
	MOV DS:[BX],AL

	MOV AL,DS:[BX+5]
	OR	AL,00100000B
	MOV DS:[BX+5],AL
	
